##知识点1：文件操作最佳实践
1. with open 上下文管理器
自动关闭文件句柄
打开模式：
r	只读	读取日志、配置文件
w	覆盖写入	全新生成文件，会清空原有内容，慎用
a	追加写入	持续记录运行日志
rb/wb	二进制读写	图片、模型、音视频文件
读写中文文件必须加 encoding="utf-8"
2. 结构化文件处理
JSON 文件
json.dump(数据, 文件句柄, ensure_ascii=False, indent=2)：字典格式写入本地 JSON，中文不乱码、格式化排版
json.load(文件句柄)：读取 JSON 文件转为 Python 字典 / 列表
CSV 文件
csv.DictReader(fp)：自动以表头为 key，每行转为字典，无需数字下标取值，可读性更高
csv.DictReader(fp,fieldnames=None)  fp with open()打开得到的文件对象  fieldnames 自定义表头，csv文件第一行没有表头的时候手动设置表头名称，字典格式
##知识点 2：正则表达式（文本清洗工具）
1.元字符
通配符. 代替任意字符但不能代替换行符\n  #\n算一个字符  如果想代替换行符可以更改模式re.s
字符集[] 表示[]内指定范围内的任意一个字符，取反范围[^]
重复元字符{} 指定左边原子的可以重复的数量范围，{0,}表示0到无穷大。默认贪婪匹配，按最大匹配数进行匹配；取消贪婪匹配，按最小匹配数进行匹配{}? ；*指定左边原子重复0次或多次等价于{0，}；+指定左边原子重复1次或多次等价于{1，}；？指定左边原子重复0次或1次等价于{0，1}在可有可无的时候使用；
字符集合重复元字符结合使用"a[a-z]{1,3}e" 表示开头是a中间两或三个字符时a-z之间的字母最后一位是e
转义符\ 1.赋予某些普通符号特殊功能 \d匹配一个数字原子 \D匹配一个非数字原子 \w匹配一个单词原子 \W匹配一个非单词原子 \b匹配一个单词边界原子；  2.取消特殊功能字符以普通花化
（）1.分组把括号里的内容当作一整个原子（？：）2.优先提取：想要规则里的一部分（），一个规则里有多个（）则有多个分组按顺序定义为group(1)、group(2)
2. re 三大核心方法
re.search(pattern, text)：查找第一个匹配项，返回 Match 对象，单行日志解析专用,想要规则里某个部分时(?P<自定义一个名字>想要的部分) ret.gruop()获取完整内容 ret.group("自定义名字")优先提取；Match对象结构：span=(开始位置，结束位置)，match="提取内容" ret.span()返回(开始位置，结束位置)；ret.start()返回开始位置； ret.edd()返回结束位置
re.findall(pattern, text)：找出全部匹配内容，返回列表，批量提取文本
re.sub()：匹配内容替换，脏数据清洗
re.compile(pattern):生成对象重复用，re.compile()描述规则 ret=re_email.findall(text) text使用前面描述的规则查找全部，适合要查找很多文本但是规则重复的程序
3. group () /groups () 区分
match.group(n)：单独取出第 n 个捕获分组内容；group(0) 代表完整匹配到的整段文本
match.groups()：将所有括号分组打包为元组，支持直接解包赋值
避坑：匹配失败 match 为 None，调用 group 直接报错，必须先 if match: 判断
##实战项目：日志清洗与关键信息提取器
1. 项目功能
自动生成模拟服务器日志文件
逐行读取日志，正则提取「时间戳、日志级别、日志详情」
仅保留 ERROR / WARNING 报错日志，过滤无关 INFO 日志
将清洗后的结构化数据导出为标准 JSON 文件
2. 核心正则解析
r"\[(.*?)\] (\w+): (.*)"
\[：匹配日志开头左方括号
(.*?)：分组 1，非贪婪匹配，提取括号内时间戳
(\w+)：分组 2，匹配 INFO/ERROR/WARNING 日志等级
(.*)：分组 3，提取冒号后全部日志描述内容
3. 输出示例（cleaned_logs.json）
[
  {
    "timestamp": "2024-05-22 10:05:23",
    "level": "ERROR",
    "message": "Connection timeout from 10.0.0.5 - User: admin"
  },
