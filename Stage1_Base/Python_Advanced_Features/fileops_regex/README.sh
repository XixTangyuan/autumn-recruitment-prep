##今日踩坑避坑总结
1.re 方法只能传入文本字符串，不能直接传文件路径，必须先用 with open 读取文件内容
2.正则特殊符号 [ ( . * + 想要作为普通文字匹配，必须加反斜杠 \ 转义
3.正则匹配后不做 if 判断，直接调用 .group() 会触发 NoneType 报错
4.文件读写忘记 encoding="utf-8"，Windows 环境中文乱码
5.w 模式写入文件会清空原有内容，生产环境记录日志优先使用 a 追加模式
