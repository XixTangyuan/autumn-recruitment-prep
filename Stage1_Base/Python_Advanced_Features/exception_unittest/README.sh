##今日踩坑记录汇总
构造函数参数语法报错
问题：__init__ 中将带默认值的正则参数写在无默认参数前面，触发 SyntaxError。
解决：把带默认值参数移至参数列表末尾。
LogCleaner 类在测试文件找不到
问题：test.py 未导入 robust 中的 LogCleaner 类。
解决：增加导入语句 from robust import LogCleaner。
单元测试仅覆盖一组过滤规则
问题：原代码只初始化 ERROR+WARNING 清洗器，未测试 INFO 单等级场景。
解决：setUp 中创建两个不同配置实例，新增对应测试方法分别校验。
测试产生的临时文件残留
问题：多次运行测试后生成大量 test 开头日志、json 文件。
解决：在 tearDown 中通过 os.path.exists 判断文件存在后自动删除。
读取空行日志匹配异常
问题：日志空行参与正则匹配造成无效遍历。
解决：增加判断，空行直接跳过匹配流程。
