##异常处理学习总结：try-except-else-finally 与 IO 异常捕获
1. try-except-else-finally 各模块作用
try：包裹可能报错、不稳定的代码（文件读写、接口请求等），程序会执行这里的逻辑，一旦出现异常立刻跳出 try 块。
except：捕获 try 中抛出的指定类型异常，自定义错误提示，避免程序直接崩溃；可分开捕获文件不存在、权限不足等细分异常。
else：仅当 try 代码块无任何异常时才执行，适合放置读取成功后的业务逻辑（如打印成功提示、数据处理）。
finally：无论是否发生异常、无论 except 是否触发，代码一定会执行；多用于资源释放、关闭文件、删除临时文件等收尾操作。
2. 工程中 IO 操作必须捕获异常的原因
文件路径不存在、文件被删除会触发 FileNotFoundError；
文件被占用、无读写权限会触发 PermissionError；
文件编码损坏、磁盘故障会抛出未知 IO 异常；
若不捕获异常，程序会直接终止运行，中断后续业务流程；
线上服务不能因单一日志文件异常整体宕机，捕获异常可打印日志、返回空数据，保证程序容错、平稳降级。
##单元测试知识点
1.前后置条件 setUp /tearDown 
前置：setUp()：每条测试方法执行前自动运行，统一初始化测试资源。本项目中自动生成测试日志文件、创建两组不同配置的 LogCleaner 实例，复用测试数据。
后置：tearDown()：每条测试方法执行完毕后自动运行，统一清理资源。本项目自动删除临时日志、输出 JSON 文件，避免文件残留污染下一轮测试。
生命周期顺序：setUp() → test_测试方法 → tearDown()，每条用例独立执行、互不干扰。
2. 常用断言用法
self.assertEqual(a, b)：判断两个值相等，用于校验日志提取数量、日志等级；
self.assertFalse(条件)：判断条件为假
self.assertTrue(条件)：判断条件为真，用于校验清洗后的 JSON 文件是否成功生成；
self.assertEqual(result, [])：校验异常场景下返回空列表。
3. 单元测试实际工作价值
代码重构防 BUG：修改 LogCleaner 过滤逻辑、正则规则后，一键运行全部用例，快速判断改动是否破坏原有功能；
边界用例自动化校验：自动覆盖正常日志、仅单一日志等级、文件不存在等边界场景，不用手动反复创建文件测试；
简化回归测试：新增功能后不需要人工逐条核对输出结果，自动化脚本快速验证全量功能；
规范代码容错能力：强制覆盖异常分支，提前暴露程序崩溃风险。
